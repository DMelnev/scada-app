using System.Collections.Generic;

namespace ScadaApp.Models;

/// <summary>
/// Корневой объект конфигурации всего приложения.
/// Содержит все настройки: устройства, база данных, 3D-визуализация.
/// Этот класс сериализуется (преобразуется) в файл config.json и
/// десериализуется (читается) из него при каждом запуске.
///
/// Сериализация — это процесс превращения объекта C# в текст (JSON).
/// Десериализация — обратный процесс: текст JSON → объект C#.
/// </summary>
public class AppConfig
{
    /// <summary>
    /// Список всех настроенных устройств (ПЛК/контроллеров).
    /// Каждый элемент списка — это один контроллер с его тегами.
    /// Пользователь управляет этим списком через окно "Настройка устройств".
    /// </summary>
    public List<DeviceConfig> Devices { get; set; } = new();

    /// <summary>
    /// Настройки базы данных: тип БД, строка подключения, интервал записи.
    /// Пользователь управляет этим через окно "База данных".
    /// </summary>
    public DatabaseConfig Database { get; set; } = new();

    /// <summary>
    /// Настройки 3D-визуализации: URL сервиса, список маппингов тег → объект.
    /// Пользователь управляет этим через окно "3D визуализация".
    /// </summary>
    public VisualizationConfig Visualization { get; set; } = new();
}
