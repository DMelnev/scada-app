using System.Collections.Generic;

namespace ScadaApp.Models;

/// <summary>
/// Конфигурация сервиса 3D-визуализации.
/// Определяет, куда отправлять данные и как значения тегов
/// связываются с объектами в 3D-сцене.
///
/// Принцип работы: при изменении значения тега приложение отправляет
/// команду во внешний 3D-движок (через WebSocket), указывая, какой объект
/// и какое его свойство нужно изменить.
///
/// Примечание: в текущей версии реальная отправка не реализована,
/// вместо неё используется заглушка (Dummy3DVisualizationService).
/// </summary>
public class VisualizationConfig
{
    /// <summary>
    /// Включён ли сервис 3D-визуализации.
    /// false — сервис не активен, данные не отправляются.
    /// true  — сервис активен и обрабатывает изменения тегов.
    /// По умолчанию: false.
    /// </summary>
    public bool Enabled { get; set; } = false;

    /// <summary>
    /// URL WebSocket-сервера 3D-визуализации.
    /// По умолчанию: "ws://localhost:8080" (локальный сервер, порт 8080).
    /// Формат: ws://адрес:порт  или  wss://адрес:порт (защищённое соединение).
    /// </summary>
    public string ServiceUrl { get; set; } = "ws://localhost:8080";

    /// <summary>
    /// Список маппингов: каждый маппинг связывает один тег с одним свойством
    /// одного объекта в 3D-сцене.
    /// Пример: тег "Температура_1" → объект "Термометр_3D" → свойство "PositionY"
    /// </summary>
    public List<TagVisualizationMapping> Mappings { get; set; } = new();
}

/// <summary>
/// Маппинг (связь) между тегом SCADA и свойством объекта в 3D-сцене.
/// Определяет, как изменение значения тега влияет на визуализацию.
/// </summary>
public class TagVisualizationMapping
{
    /// <summary>
    /// Имя тега (должно совпадать с полем Name в TagConfig).
    /// При изменении значения этого тега будет обновляться 3D-объект.
    /// Пример: "Температура_1", "Клапан_Главный"
    /// </summary>
    public string TagName { get; set; } = "";

    /// <summary>
    /// Имя 3D-объекта в сцене внешнего 3D-движка.
    /// Объект с таким именем должен существовать в 3D-сцене.
    /// Пример: "Термометр", "Клапан_001", "Стрелка_давления"
    /// </summary>
    public string ObjectName { get; set; } = "";

    /// <summary>
    /// Свойство 3D-объекта, которое нужно изменить при обновлении тега.
    /// Возможные значения:
    ///   "PositionX" / "PositionY" / "PositionZ" — позиция в пространстве
    ///   "RotationX" / "RotationY" / "RotationZ" — угол поворота
    ///   "State"     — произвольное состояние объекта
    /// Конкретный набор поддерживаемых свойств зависит от 3D-движка.
    /// </summary>
    public string Property { get; set; } = "";
}
