using System;

namespace ScadaApp.Models;

/// <summary>
/// Конфигурация одного тега — переменной в памяти промышленного контроллера.
///
/// Тег (Tag) в SCADA-системах — это именованная переменная, соответствующая
/// определённому адресу в памяти ПЛК. Теги представляют физические сигналы:
/// температуру, давление, состояние клапана, счётчик и т.д.
///
/// Для Siemens S7 адрес тега выглядит как "DB1.DBD0" (блок данных 1, смещение 0).
/// </summary>
public class TagConfig
{
    /// <summary>
    /// Уникальный идентификатор тега (GUID).
    /// Генерируется автоматически. Используется для внутреннего отслеживания тегов.
    /// </summary>
    public string Id { get; set; } = Guid.NewGuid().ToString();

    /// <summary>
    /// Понятное имя тега для отображения в таблице главного окна.
    /// Придумывается пользователем. Пример: "Температура_двигателя", "Давление_бак".
    /// Это имя используется при поиске тега в событии TagValueChanged.
    /// </summary>
    public string Name { get; set; } = "";

    /// <summary>
    /// Адрес переменной в памяти ПЛК Siemens S7.
    /// Формат зависит от типа данных и области памяти:
    ///   DB1.DBX0.0  — бит в блоке данных (Bool)
    ///   DB1.DBB2    — байт в блоке данных (Byte)
    ///   DB1.DBW4    — слово (Word, 2 байта)
    ///   DB1.DBD0    — двойное слово (DWord/Int/Real, 4 байта)
    ///   M0.0        — бит в области флагов
    ///   MW10        — слово флагов
    /// Адреса задаются в программе ПЛК (например, в TIA Portal).
    /// </summary>
    public string Address { get; set; } = "";

    /// <summary>
    /// Тип данных тега — должен совпадать с типом переменной в программе ПЛК.
    /// Возможные значения:
    ///   "Bool"  — логический тип, True/False (1 бит)
    ///   "Byte"  — беззнаковое целое 8 бит (0..255)
    ///   "Word"  — беззнаковое целое 16 бит (0..65535)
    ///   "DWord" — беззнаковое целое 32 бита
    ///   "Int"   — знаковое целое 16 бит (-32768..32767)
    ///   "DInt"  — знаковое целое 32 бита
    ///   "Real"  — число с плавающей точкой (аналог float в C#)
    ///   "String"— строка символов
    /// По умолчанию: "Real" (наиболее часто используемый тип для аналоговых сигналов).
    /// </summary>
    public string DataType { get; set; } = "Real";

    /// <summary>
    /// Необязательное текстовое описание тега.
    /// Используется только для удобства пользователя, не влияет на работу опроса.
    /// Пример: "Температура подшипника двигателя М1, датчик PT100"
    /// </summary>
    public string Description { get; set; } = "";

    /// <summary>
    /// Флаг активности тега.
    /// true  — тег участвует в опросе (значение читается при каждом цикле)
    /// false — тег пропускается при опросе (удобно для временного отключения)
    /// По умолчанию: true.
    /// </summary>
    public bool Enabled { get; set; } = true;
}
