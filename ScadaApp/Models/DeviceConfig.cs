using System;
using System.Collections.Generic;

namespace ScadaApp.Models;

/// <summary>
/// Конфигурация одного промышленного контроллера (ПЛК / PLC).
/// Хранит все параметры, необходимые для подключения к устройству,
/// а также список тегов (переменных), которые нужно читать с него.
///
/// PLC (Programmable Logic Controller) — программируемый логический контроллер,
/// промышленный компьютер, управляющий оборудованием на производстве.
/// </summary>
public class DeviceConfig
{
    /// <summary>
    /// Уникальный идентификатор устройства.
    /// Генерируется автоматически при создании устройства (GUID — Globally Unique Identifier).
    /// Используется для связи устройства с его тегами и драйвером внутри приложения.
    /// НЕ меняйте это поле вручную в config.json.
    /// </summary>
    public string Id { get; set; } = Guid.NewGuid().ToString();

    /// <summary>
    /// Произвольное имя устройства, отображаемое в интерфейсе.
    /// Пример: "ПЛК_Линия_1", "Насос_Главный", "SIMATIC_S7-1500_Цех1"
    /// </summary>
    public string Name { get; set; } = "";

    /// <summary>
    /// Тип протокола/драйвера для подключения к устройству.
    /// "Siemens" — для контроллеров Siemens S7 (ISO on TCP, порт 102).
    /// "OpcUa"   — зарезервировано для будущей поддержки протокола OPC UA.
    /// По умолчанию: "Siemens".
    /// </summary>
    public string DriverType { get; set; } = "Siemens";

    /// <summary>
    /// IP-адрес контроллера в локальной сети.
    /// Пример: "192.168.0.10", "10.0.0.5".
    /// Компьютер должен иметь сетевой доступ к этому адресу.
    /// </summary>
    public string IpAddress { get; set; } = "";

    /// <summary>
    /// Номер стойки (Rack) в конфигурации оборудования Siemens.
    /// Определяет физическое местоположение CPU в монтажной стойке.
    /// Для большинства установок: 0.
    /// Уточняйте у инженера по оборудованию.
    /// </summary>
    public int Rack { get; set; } = 0;

    /// <summary>
    /// Номер слота (Slot) — позиция CPU-модуля в стойке.
    /// Типичные значения:
    ///   S7-1200 / S7-1500 → 1
    ///   S7-300            → 2
    /// По умолчанию: 1.
    /// </summary>
    public int Slot { get; set; } = 1;

    /// <summary>
    /// TCP-порт для подключения.
    /// Стандартный порт протокола ISO on TCP для Siemens S7: 102.
    /// Обычно не требует изменения.
    /// </summary>
    public int Port { get; set; } = 102;

    /// <summary>
    /// Точная модель CPU-модуля контроллера Siemens.
    /// Влияет на протокол обмена данными (разные модели используют
    /// немного разные варианты протокола S7).
    /// Допустимые значения: "S71200", "S71500", "S7300", "S7400", "S7200".
    /// По умолчанию: "S71500".
    /// </summary>
    public string CpuType { get; set; } = "S71500";

    /// <summary>
    /// Интервал между циклами опроса устройства в миллисекундах.
    /// Определяет, как часто приложение читает все теги этого устройства.
    /// По умолчанию: 1000 (1 секунда).
    /// Слишком малый интервал (менее 200 мс) может перегрузить ПЛК.
    /// </summary>
    public int PollingIntervalMs { get; set; } = 1000;

    /// <summary>
    /// Список тегов (переменных), которые нужно читать с этого устройства.
    /// Каждый тег — это отдельная переменная в памяти ПЛК (температура, давление и т.д.).
    /// Пользователь управляет этим списком через окно "Настройка тегов".
    /// </summary>
    public List<TagConfig> Tags { get; set; } = new();
}
